package forAndWhileLoopEg;

public class WhileLoopAssign {

	public static void main(String[] args)
	{
		char i='a';
		while (i<='z')
		{
			System.out.println(i);
			i++;
		}
		System.out.println("+++++++++++++++++++");
		char j ='z';
		while (j>='a')
		{
			System.out.println(j );
			j--;	
		}

	}
}
