package LoopStudy7;

public class ForLoopUse {

	public static void main(String[] args) 
	{
		//incremental for loop
		for (char i='a'; i<='z'; i++)
		{
			System.out.println(i);
		}
		System.out.println("===============================");
		
		// decremental for loop
		for (char i='z'; i>='a'; i--)
		{
			System.out.println(""+i);
		}

	}

}
