package LoopStudy7;

public class Test {

	public static void main(String[] args) {
		int a=10;
		System.out.println("Old value "+a);
		++a;	//per increment
		a++;	//post increment a++; ===>a=a+1;

		System.out.println("New value is "+a);
		
		int b=20;
		System.out.println("Old value "+b);
		--b;	//per decrement
		b--;	//post decrement b--; ===>b=b-1;

		System.out.println("New value is "+b);
		
	
	}

}
