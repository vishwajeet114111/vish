package TP;

public class MulitDimArray {

	public static void main(String [] args) 
	{
	
		int vish[][]= {{1,2},{3,4},{5,6}};
		
		for(int i =0; i<=vish.length-1;i++)
		{
			for(int j =0;j<=1;j++)
			{
				System.out.print(vish[i][j]+" ");
			}
				System.out.println();
				
		}}
				
				
				
				
		

	}

